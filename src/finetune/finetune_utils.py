def get_fc_weight(dataset, processor, model:CLIPModel):
    text_template = get_clip_text_template(dataset)
    inputs = processor(text=text_template, return_tensors="pt", padding=True)

    with torch.no_grad():
        text_features = model.get_text_features(**inputs)  

    text_features = text_features / text_features.norm(dim=-1, keepdim=True)
#     fc_weight = text_features
    logit_scale = torch.tensor(model.config.logit_scale_init_value).exp()
    fc_weights = text_features * logit_scale
    print(fc_weights.shape)
    return fc_weights